// Language Toggle
function initLanguage() {
    const langButtons = document.querySelectorAll('.lang-btn');

    langButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
        });
    });

    setLanguage(currentLang);
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    sessionStorage.setItem('chatLang', lang);
    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // Update all translatable elements
    document.querySelectorAll('[data-i18n], [data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const placeholderKey = el.getAttribute('data-i18n-placeholder');

        if (key) {
            const keys = key.split('.');
            let value = translations[lang];
            for (let k of keys) value = value?.[k];
            if (value) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    if (el.type === 'submit' || el.type === 'button') el.value = value;
                    else el.placeholder = value;
                } else if (el.tagName === 'SELECT') {
                    // Update select options if they have data-i18n
                    Array.from(el.options).forEach(opt => {
                        const optKey = opt.getAttribute('data-i18n');
                        if (optKey) {
                            let optValue = translations[lang];
                            for (let k of optKey.split('.')) optValue = optValue?.[k];
                            if (optValue) opt.textContent = optValue;
                        }
                    });
                } else {
                    el.innerHTML = value;
                }
            }
        }

        if (placeholderKey) {
            const keys = placeholderKey.split('.');
            let value = translations[lang];
            for (let k of keys) value = value?.[k];
            if (value) el.placeholder = value;
        }
    });

    // Update language-specific content blocks (e.g. legal pages)
    document.querySelectorAll('[data-lang-content]').forEach(el => {
        const contentLang = el.getAttribute('data-lang-content');
        if (contentLang === lang) {
            el.style.display = 'block';
        } else {
            el.style.display = 'none';
        }
    });

    // Update meta title
    if (lang === 'sk') {
        document.title = 'ArciGy — Automatizácia Procesov | Architekti Efektivity';
    } else {
        document.title = 'ArciGy — Business Process Automation | Efficiency Architects';
    }

    // --- Calendar Language Sync ---
    // If the calendar has been opened, re-render it to reflect the new language
    if (window.arcigyCalendar && typeof window.arcigyCalendar.updateLanguage === 'function') {
        window.arcigyCalendar.updateLanguage(lang);
    }

    // --- Cookie Consent Language Sync ---
    if (typeof window.updateCookieConsentLanguage === 'function') {
        window.updateCookieConsentLanguage(lang);
    }
}

// Navigation
function initNavigation() {
    const navToggle = document.getElementById('navToggle');
    const navMenu = document.getElementById('navMenu');
    const navLinks = document.querySelectorAll('.nav-link');

    if (navToggle && navMenu) {
        navToggle.addEventListener('click', () => {
            const isActive = navMenu.classList.toggle('active');
            navToggle.classList.toggle('active');

            // If opening menu on mobile, close other overlays
            if (isActive && window.innerWidth <= 768) {
                if (window.closeChat) window.closeChat();
                if (window.arcigyCalendar && window.arcigyCalendar.close) window.arcigyCalendar.close();
            }
        });
    }

    // Close menu on link click (mobile)
    navLinks.forEach(link => {
        link.addEventListener('click', () => {
            if (window.innerWidth <= 768) {
                navMenu.classList.remove('active');
                navToggle.classList.remove('active');
            }
        });
    });

    // Scrolled navbar state
    const navbar = document.getElementById('navbar');
    if (navbar) {
        const updateNavbar = () => {
            if (window.scrollY > 50) {
                navbar.classList.add('scrolled');
            } else {
                navbar.classList.remove('scrolled');
            }
        };

        // Initial check
        updateNavbar();

        window.addEventListener('scroll', updateNavbar, { passive: true });
    }
}

// Scroll Animations
function initAnimations() {
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            } else {
                // Remove visible class when out of view to re-trigger animation
                entry.target.classList.remove('visible');
            }
        });
    }, observerOptions);

    // Observe all animatable elements
    document.querySelectorAll('.problem-card, .service-item, .process-step, .pricing-card, .testimonial-card, .value-card, .team-card, .blog-card, .position-card, .step-card').forEach(el => {
        observer.observe(el);
    });

    // Removed scroll-based parallax for hero shapes (to keep visuals static on scroll)
}

